public class OperatorPractice2 {
    public static void main(String[] args) {

        int a = 40; // a=40->39

        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);









    }
}
