package day08_relational_operators;

public class OperatorPractice {
    public static void main(String[] args) {

        int b = 10; //b=10
        b++; // post increment
        System.out.println(b);
        b--;
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b++);
        System.out.println(b);




    }
}
