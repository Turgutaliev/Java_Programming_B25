package day23_loops;

public class ForLoopExample {
    public static void main(String[] args) {

        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }

        //same as above 
        int z = 1;
        while (z<=10){
            System.out.println(z);
            z++;
        }






    }
}
