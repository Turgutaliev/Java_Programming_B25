package day22_loops;

public class DoWhileExample {
    public static void main(String[] args) {


        int a =0;

        do{
            System.out.print(a);
            a++;
        }while (a<10);


        System.out.println(" ");

        int a1 =  0;

        do{
            System.out.print(a1);
            a1++;
        }while (a1==10);


        int a2 = 0;

        while (a2==10) {
            System.out.println(a2);
            a2++;
        }
    }
}
